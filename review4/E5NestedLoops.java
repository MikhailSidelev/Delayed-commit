package review4;

public class E5NestedLoops {
    public static void main(String[] args) {
        /*
         *
         * *
         * * *
         * * * *
         * * *
         * *
         *
         */
        for (int i = 1; i <= 4; i++) {
            System.out.println("*");

        }
        for (int i = 0; i < 1; i++) {
            System.out.println("*");
        }
        for (int i = 0; i < 2; i++) {
            System.out.println("*");
        }
        for (int i = 0; i < 3; i++) {
            System.out.println("*");
        }
        for (int i = 0; i < 4; i++) {
            System.out.println("*");
        }
    }
}

