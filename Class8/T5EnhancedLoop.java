public class T5EnhancedLoop {

    public static void main(String[] args) {
           // to find even numbers
        int[] nums = {10, 23, 25, 56, 45, 20};

        for (int j : nums) {
            if (j % 2 == 0) {

                System.out.println(j);

            }

        }


    }
}